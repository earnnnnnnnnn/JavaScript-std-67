// กำหนดค่าของตัวแปร num1 เป็น 1 (ชนิดข้อมูลตัวเลข) และ num2 เป็น '1' (ชนิดข้อมูลสตริง)
let num1 = 1;
let num2 = '1';

// การใช้เครื่องหมาย + ในการบวกค่า
// เมื่อบวก num1 (ตัวเลข) กับ num2 (สตริง) จะเกิดการแปลงค่า num1 เป็นสตริงและเชื่อมเข้ากับ num2
console.log(num1 + num2); // แสดงผล: "11"

// การบวก num1 (ตัวเลข) กับ 1 (ตัวเลข)
// ผลลัพธ์จะเป็นการบวกตัวเลข 1 + 1 ซึ่งจะได้ผลลัพธ์เป็น 2
console.log(num1 + 1); // แสดงผล: 2

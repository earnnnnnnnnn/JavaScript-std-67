function removeDups(array) {
    return [...new Set(array)]; // ใช้ Set เพื่อลบค่าซ้ำ
}

console.log(removeDups([1, 0, 1, 0]));
console.log(removeDups(["The", "big", "cat"]));
console.log(removeDups(["John", "Taylor", "John"]));

// วิธีทำงาน:
// Set จะเก็บค่าเฉพาะที่ไม่ซ้ำกัน เมื่อแปลงค่าจาก Set กลับเป็นอาร์เรย์ ก็จะได้อาร์เรย์ที่ไม่มีค่าซ้ำ
// การแปลงจาก Set เป็นอาร์เรย์: ทำได้ด้วยการใช้ [...new Set(array)]
// ข้อดี:
// ฟังก์ชันนี้สามารถใช้ได้ทั้งกับ ตัวเลขและสตริง
// ใช้ Set ซึ่งมีประสิทธิภาพในการลบค่าซ้ำได้รวดเร็ว
// การใช้ ...new ใน JavaScript คือการใช้ spread syntax (...) ร่วมกับ Set 
// เพื่อแปลงค่าภายใน Set ไปเป็นอาร์เรย์ ซึ่งช่วยให้สามารถรับค่าจาก 
// Set ที่ไม่ซ้ำกันและแปลงเป็นอาร์เรย์ได้
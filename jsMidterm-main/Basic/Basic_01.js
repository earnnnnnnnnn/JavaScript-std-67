function numberSplit(num) {
    const result = [];  // สร้างอาร์เรย์เปล่าที่จะเก็บผลลัพธ์

    // ถ้าหมายเลขที่ให้เข้ามาเป็นเลขคู่ (หาร 2 ลงตัว)
    if (num % 2 === 0) {    
        // ถ้าเป็นเลขคู่ จะหาผลลัพธ์โดยการหาร 2 แล้วเก็บทั้งสองค่าที่ได้ไว้ในอาร์เรย์
        result.push(num / 2);  // ผลลัพธ์ที่ได้จากการหาร 2
        result.push(num / 2);  // เก็บผลลัพธ์ซ้ำอีกครั้งในอาร์เรย์
    } else {   // ถ้าไม่ใช่เลขคู่ (คือเลขคี่)
        // ถ้าเป็นเลขคี่ จะหาค่าต่ำสุดและสูงสุดจากการหาร 2 โดยใช้ Math.floor() และ Math.ceil()
        result.push(Math.floor(num / 2));  // ค่าเป็นจำนวนเต็มที่ต่ำสุดจากการหาร
        result.push(Math.ceil(num / 2));   // ค่าเป็นจำนวนเต็มที่สูงสุดจากการหาร
    }

    return result;  // ส่งคืนอาร์เรย์ผลลัพธ์ที่เก็บค่าแล้ว
}

console.log(numberSplit(4));  // ผลลัพธ์ที่ได้คือ [2, 2] เพราะ 4 เป็นเลขคู่
console.log(numberSplit(10)); // ผลลัพธ์ที่ได้คือ [5, 5] เพราะ 10 เป็นเลขคู่
console.log(numberSplit(11)); // ผลลัพธ์ที่ได้คือ [5, 6] เพราะ 11 เป็นเลขคี่
console.log(numberSplit(-9)); // ผลลัพธ์ที่ได้คือ [-5, -4] เพราะ -9 เป็นเลขคี่

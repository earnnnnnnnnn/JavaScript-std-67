function findPos(pos) {
    // สร้างอาร์เรย์ data ที่มีความยาว 41 ตำแหน่ง
    const data = Array(41);
    let posmove = 31; // กำหนดตำแหน่งเริ่มต้นของผู้เล่นให้เริ่มต้นที่ตำแหน่ง 31
    data[31] = 'player'; // ตำแหน่งเริ่มต้นของผู้เล่นในอาร์เรย์

    // วนลูปผ่านข้อมูลในอาร์เรย์ pos
    for (let i = 0; i < pos.length; i += true) {
        let position = pos[i].toUpperCase(); // เปลี่ยนอักขระที่ได้เป็นตัวพิมพ์ใหญ่

        // ตรวจสอบทิศทางการเคลื่อนที่ของผู้เล่น
        if (position === 'N') { // ทิศเหนือ
            data.splice(posmove, 1); // ลบตำแหน่งเดิมของผู้เล่น
            data[posmove -= 7] = 'player'; // ย้ายผู้เล่นขึ้นไป 7 ตำแหน่ง (ตำแหน่งด้านบน)

        } else if (position === 'E') { // ทิศตะวันออก
            data.splice(posmove, 1); // ลบตำแหน่งเดิมของผู้เล่น
            data[posmove += 1] = 'player'; // ย้ายผู้เล่นไปข้างขวา 1 ตำแหน่ง

        } else if (position === 'W') { // ทิศตะวันตก
            data.splice(posmove, 1); // ลบตำแหน่งเดิมของผู้เล่น
            data[posmove -= 1] = 'player'; // ย้ายผู้เล่นไปข้างซ้าย 1 ตำแหน่ง

        } else if (position === 'S') { // ทิศใต้
            data.splice(posmove, 1); // ลบตำแหน่งเดิมของผู้เล่น
            data[posmove += 7] = 'player'; // ย้ายผู้เล่นลงไป 7 ตำแหน่ง (ตำแหน่งด้านล่าง)
        }
    }

    // ตรวจสอบว่ามีการป้อนคำสั่งที่ไม่ถูกต้องหรือไม่
    if (pos.some(direction => [ // ตรวจสอบอักขระที่ไม่ใช่ N, E, W, S
            'A', 'B', 'C', 'D', 'F',
            'G', 'H', 'I', 'J', 'K',
            'L', 'M', 'O', 'P', 'Q',
            'R', 'T', 'U', 'V', 'X',
            'Y', 'Z'
        ].includes(direction))) console.log('GAME OVER!, YOU FOUL!!!'); // แสดงข้อความว่าเกมจบ เนื่องจากมีการป้อนคำสั่งผิด

    // ตรวจสอบตำแหน่งสุดท้ายของผู้เล่น
    else if (data[13] == 'player') console.log('Last Position : TG1 , You WIN!'); // หากผู้เล่นอยู่ที่ตำแหน่ง 13 (TG1) ถือว่าชนะ
    else if (data[14] == 'player') console.log('Last Position : TG2 , You WIN!'); // หากผู้เล่นอยู่ที่ตำแหน่ง 14 (TG2) ถือว่าชนะ
    else console.log(`Last Position : ${posmove}  , You LOSE! :()`); // หากผู้เล่นไม่ถึงตำแหน่งเป้าหมาย ให้แสดงตำแหน่งสุดท้ายและผลลัพธ์ว่าแพ้
}

// กำหนดชุดการเคลื่อนที่
const pos1 = ['n', 'e', 'n', 'E', 'e', 'n']; // เคลื่อนที่ไปทางเหนือและตะวันออก
const pos2 = ['n', 'e', 'e', 'N', 'e', 'n']; // เคลื่อนที่ไปทางเหนือและตะวันออกหลายครั้ง
const pos3 = ['n', 'e', 'W', 'w', 'w', 'w', 'n']; // เคลื่อนที่ไปทางเหนือและทางตะวันตก
const pos4 = ['n', 'e', 'n', 'e', 'e', 's']; // เคลื่อนที่ไปทางเหนือ ตะวันออก และใต้
const pos5 = ['w', 'e', 'n', 'X', 'e', 's']; // มีคำสั่งผิดพลาด ('X')

// เรียกใช้ฟังก์ชันด้วยชุดการเคลื่อนที่ต่าง ๆ
findPos(pos1); // ทดสอบชุดที่ 1
findPos(pos2); // ทดสอบชุดที่ 2
findPos(pos3); // ทดสอบชุดที่ 3
findPos(pos4); // ทดสอบชุดที่ 4
findPos(pos5); // ทดสอบชุดที่ 5
